class Solution:
    """
    @param: A: An integer array
    @param: B: An integer array
    @return: Their smallest difference.
    """
    def smallestDifference(self, A, B):
        # write your code here
        
        sa = sorted(A)
        sb = sorted(B)

        dist = abs(sa[0] - sb[0])

        pp = 0
        for i in range(len(sa)):
            while pp + 1 < len(sb) and abs(sa[i] - sb[pp]) >= abs(sa[i] - sb[pp + 1]):
                pp += 1
            if dist > abs(sa[i] - sb[pp]):
                dist = abs(sa[i] - sb[pp])
                
        return dist

